BOOL CMymenuApp::InitInstance()
{
	AfxEnableControlContainer();
	
	// Standard initialization
	// If you are not using these features and wish to reduce the size
	//  of your final executable, you should remove from the following
	//  the specific initialization routines you do not need.
	
#ifdef _AFXDLL
	Enable3dControls();			// Call this when using MFC in a shared DLL
#else
	Enable3dControlsStatic();	// Call this when linking to MFC statically
#endif
	
	// Change the registry key under which our settings are stored.
	// You should modify this string to be something appropriate
	// such as the name of your company or organization.
	SetRegistryKey(_T("Local AppWizard-Generated Applications"));
	
	LoadStdProfileSettings();  // Load standard INI file options (including MRU)
	
	CCommandLineInfo cmdInfo;
	ParseCommandLine(cmdInfo);
	// This chunk of code prevents the default view from being loaded
	//if(cmdInfo.m_nShellCommand==CCommandLineInfo::FileNew){
	//	cmdInfo.m_nShellCommand = CCommandLineInfo::FileNothing;
	//}
	
	// Register the application's document templates.  Document templates
	//  serve as the connection between documents, frame windows and views.
	
	CMultiDocTemplate* pDocTemplate;
	pDocTemplate = new CMultiDocTemplate(
		IDR_MYMENUTYPE,
		RUNTIME_CLASS(CMymenuDoc),
		RUNTIME_CLASS(CChildFrame), // custom MDI child frame
		RUNTIME_CLASS(CMymenuView));
	AddDocTemplate(pDocTemplate);
	pMyTemplate = new CMultiDocTemplate(
		IDR_MYTYPE,
		RUNTIME_CLASS(CMymenuDoc),
		RUNTIME_CLASS(CMyFrame),
		RUNTIME_CLASS(CMyView));
	AddDocTemplate(pMyTemplate);
	
	// create main MDI Frame window
	CMainFrame* pMainFrame = new CMainFrame;
	if (!pMainFrame->LoadFrame(IDR_MAINFRAME))
		return FALSE;
	m_pMainWnd = pMainFrame;
	
	// This code replaces the MFC created menus with the Ownerdrawn versions 
	pDocTemplate->m_hMenuShared=pMainFrame->NewMenu();
	pMyTemplate->m_hMenuShared=pMainFrame->NewMyMenu();
	pMainFrame->m_hMenuDefault=pMainFrame->NewDefaultMenu();
	
	// This simulates a window being opened if you don't have
	// a default window displayed at startup
	pMainFrame->OnUpdateFrameMenu(pMainFrame->m_hMenuDefault);
	
	
	// Dispatch commands specified on the command line
	if (!ProcessShellCommand(cmdInfo))
		return FALSE;
	
	// The main window has been initialized, so show and update it.
	pMainFrame->ShowWindow(m_nCmdShow);
	pMainFrame->UpdateWindow();
	
	return TRUE;
}

